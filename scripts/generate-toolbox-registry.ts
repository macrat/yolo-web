/**
 * Tile definition entry — collected from optional `tile.ts` files under content directories.
 *
 * Used as the input type for buildTileDefinitionsRegistryContent().
 */
export interface TileDefinitionEntry {
  /** Content slug (matches the slug in meta.ts for the same directory). */
  slug: string;
  /**
   * Source path of the tile.ts file relative to the project root.
   * Used to generate the correct @/ import path in the output file.
   * Examples:
   *   "src/tools/json-formatter/tile.ts"
   *   "src/play/games/irodori/tile.ts"
   *   "src/play/quiz/kanji-level/tile.ts"
   */
  contentPath: string;
}

/**
 * Convert a content path like "src/tools/json-formatter/tile.ts"
 * to an @/ alias import path like "@/tools/json-formatter/tile".
 */
function contentPathToImportPath(contentPath: string): string {
  // Remove leading "src/" and trailing ".ts" extension
  const withoutSrc = contentPath.replace(/^src\//, "");
  const withoutExt = withoutSrc.replace(/\.ts$/, "");
  return `@/${withoutExt}`;
}

/**
 * Generate the TypeScript source content for tile-definitions-registry.ts.
 *
 * Exported as a pure function so it can be unit-tested in-memory
 * without touching the filesystem.
 *
 * Phase 7.3 (CRIT-5): tile definitions may be 0 at generation time.
 * No assertMinCount check is added here — Phase 8 will add it once the first
 * tile implementation exists.
 *
 * @param tileEntries - sorted list of tile definition entries discovered from tile.ts files
 * @returns TypeScript source string (without trailing newline — caller adds it)
 */
export function buildTileDefinitionsRegistryContent(
  tileEntries: TileDefinitionEntry[],
): string {
  const lines: string[] = [
    "/**",
    " * AUTO-GENERATED FILE — DO NOT EDIT MANUALLY.",
    " *",
    " * Generated by: scripts/generate-toolbox-registry.ts",
    " * Regenerate with: npm run generate:toolbox-registry",
    " *",
    " * If you find a manual edit here, run `npm run generate:toolbox-registry`",
    " * to restore. The git diff makes unintended edits easy to detect and revert.",
    " *",
    " * This file collects optional tile definitions from content directories.",
    " * Each content's `tile.ts` exports a `tileDef: TileDefinition` const.",
    " * The public API is exposed via src/lib/toolbox/registry.ts — callers should",
    " * never import this file directly.",
    " *",
    " * SCOPE (Phase 7.3):",
    " *   - src/tools/{slug}/tile.ts",
    " *   - src/play/games/{slug}/tile.ts",
    " *   - src/play/quiz/{slug}/tile.ts",
    " *   - src/play/fortune/{slug}/tile.ts",
    " * OUT OF SCOPE: cheatsheets / dictionary (Phase 9.2 / 9.3).",
    " *",
    " * COUNT at generation time is 0 until Phase 8 adds individual tile implementations.",
    " */",
    "",
    'import type { TileDefinition } from "../tile-types";',
    "",
  ];

  if (tileEntries.length === 0) {
    lines.push(
      "// No tile.ts files found yet. Phase 8 will add individual tile implementations.",
    );
    lines.push("");
    lines.push("export const allTileDefinitions: TileDefinition[] = [];");
  } else {
    lines.push(
      "// --- Tile definition imports (auto-discovered from tile.ts files) ---",
    );
    for (const entry of tileEntries) {
      const importName = `tile_${entry.slug.replace(/-/g, "_")}`;
      const importPath = contentPathToImportPath(entry.contentPath);
      lines.push(`import { tileDef as ${importName} } from "${importPath}";`);
    }

    lines.push("");
    lines.push("export const allTileDefinitions: TileDefinition[] = [");
    for (const entry of tileEntries) {
      const importName = `tile_${entry.slug.replace(/-/g, "_")}`;
      lines.push(`  ${importName},`);
    }
    lines.push("];");
  }

  lines.push("");
  lines.push(`// Count at generation time: tiles=${tileEntries.length}`);

  return lines.join("\n");
}

/**
 * Generate the TypeScript source content for toolbox-registry.ts.
 *
 * Exported as a pure function so it can be unit-tested in-memory
 * without touching the filesystem (scripts/__tests__/generate-toolbox-registry.test.ts).
 *
 * @param toolSlugs - sorted list of tool slugs discovered from src/tools/{slug}/meta.ts
 * @param playCount - count of allPlayContents at generation time (for the comment)
 * @returns TypeScript source string (without trailing newline — caller adds it)
 */
export function buildRegistryContent(
  toolSlugs: string[],
  playCount: number,
): string {
  const lines: string[] = [
    "/**",
    " * AUTO-GENERATED FILE — DO NOT EDIT MANUALLY.",
    " *",
    " * Generated by: scripts/generate-toolbox-registry.ts",
    " * Regenerate with: npm run generate:toolbox-registry",
    " *",
    " * If you find a manual edit here, run `npm run generate:toolbox-registry`",
    " * to restore. The git diff makes unintended edits easy to detect and revert.",
    " *",
    " * This file merges all tool / play entries into a unified",
    " * Tileable[] array.  The public API (getAllTileables, getTileableBySlug)",
    " * is exposed via src/lib/toolbox/registry.ts which",
    " * imports from here — callers should never import this file directly.",
    " *",
    " * TOOLS are auto-discovered from meta.ts files.",
    " * PLAY is imported from the existing src/play/registry.ts because play",
    " * content does not follow the per-slug meta.ts convention.",
    " */",
    "",
    'import type { Tileable } from "../types";',
    'import { toTileable } from "../types";',
    "",
    "// --- Tool imports (auto-discovered from src/tools/{slug}/meta.ts) ---",
  ];

  // Tool imports
  for (const slug of toolSlugs) {
    const importName = `tool_${slug.replace(/-/g, "_")}`;
    lines.push(`import { meta as ${importName} } from "@/tools/${slug}/meta";`);
  }

  lines.push("");
  lines.push(
    "// --- Play import (not auto-discovered; uses existing registry) ---",
  );
  lines.push('import { allPlayContents } from "@/play/registry";');
  lines.push("");
  lines.push("// --- Unified Tileable arrays ---");
  lines.push("");
  lines.push(
    "/** All tool Tileables (auto-discovered from src/tools/{slug}/meta.ts). */",
  );
  lines.push("export const toolTileables: Tileable[] = [");
  for (const slug of toolSlugs) {
    const importName = `tool_${slug.replace(/-/g, "_")}`;
    lines.push(`  toTileable(${importName}, "tool"),`);
  }
  lines.push("];");
  lines.push("");
  lines.push(
    "/** All play Tileables (sourced from existing src/play/registry.ts). */",
  );
  lines.push("export const playTileables: Tileable[] = [");
  lines.push('  ...allPlayContents.map((meta) => toTileable(meta, "play")),');
  lines.push("];");
  lines.push("");
  lines.push(
    `// Counts at generation time: tools=${toolSlugs.length}, play=${playCount} (from src/play/registry.ts)`,
  );

  return lines.join("\n");
}

async function main(): Promise<void> {
  const startTime = Date.now();

  // --- Discover meta.ts files ---
  const toolPaths = await fg("src/tools/*/meta.ts", { cwd: ROOT });
  const cheatsheetPaths = await fg("src/cheatsheets/*/meta.ts", {
    cwd: ROOT,
  });

  // Sort for deterministic output (git diff stays clean on re-generation)
  toolPaths.sort();
  cheatsheetPaths.sort();

  const toolSlugs = toolPaths.map(slugFromPath);
  const cheatsheetSlugs = cheatsheetPaths.map(slugFromPath);
  const playCount = allPlayContents.length;

  // --- Sanity checks (early-fail on unexpected structural changes) ---
  assertMinCount("tools", toolSlugs.length, 10);

  // --- Generate and write: toolbox unified registry ---
  const toolboxContent = buildRegistryContent(toolSlugs, playCount) + "\n";
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }
  fs.writeFileSync(OUTPUT_FILE, toolboxContent, "utf-8");

  // --- Generate and write: tools-only registry ---
  const toolsContent = buildToolsRegistryContent(toolSlugs) + "\n";
  const toolsOutputDir = path.dirname(TOOLS_OUTPUT_FILE);
  if (!fs.existsSync(toolsOutputDir)) {
    fs.mkdirSync(toolsOutputDir, { recursive: true });
  }
  fs.writeFileSync(TOOLS_OUTPUT_FILE, toolsContent, "utf-8");

  // --- Generate and write: cheatsheets-only registry ---
  const cheatsheetsContent =
    buildCheatsheetRegistryContent(cheatsheetSlugs) + "\n";
  const cheatsheetsOutputDir = path.dirname(CHEATSHEETS_OUTPUT_FILE);
  if (!fs.existsSync(cheatsheetsOutputDir)) {
    fs.mkdirSync(cheatsheetsOutputDir, { recursive: true });
  }
  fs.writeFileSync(CHEATSHEETS_OUTPUT_FILE, cheatsheetsContent, "utf-8");

  // --- Discover tile.ts files (Phase 7.3) ---
  // Collect optional tile definitions from tools and play content directories.
  // cheatsheets / dictionary are out of scope (Phase 9.2 / 9.3).
  // Play content without per-slug directories (daily, shared registries) is
  // naturally excluded because no {slug}/tile.ts file path exists for them.
  const tilePaths = await fg(
    [
      "src/tools/*/tile.ts",
      "src/play/games/*/tile.ts",
      "src/play/quiz/*/tile.ts",
      "src/play/fortune/*/tile.ts",
    ],
    { cwd: ROOT },
  );

  // Sort for deterministic output
  tilePaths.sort();

  const tileEntries: TileDefinitionEntry[] = tilePaths.map((filePath) => ({
    slug: slugFromPath(filePath),
    contentPath: filePath,
  }));

  // --- Generate and write: tile definitions registry ---
  // NOTE: 0 entries is valid at Phase 7.3 completion. No assertMinCount here (CRIT-5).
  const tileDefinitionsContent =
    buildTileDefinitionsRegistryContent(tileEntries) + "\n";
  const tileDefinitionsOutputDir = path.dirname(TILE_DEFINITIONS_OUTPUT_FILE);
  if (!fs.existsSync(tileDefinitionsOutputDir)) {
    fs.mkdirSync(tileDefinitionsOutputDir, { recursive: true });
  }
  fs.writeFileSync(
    TILE_DEFINITIONS_OUTPUT_FILE,
    tileDefinitionsContent,
    "utf-8",
  );

  const elapsed = Date.now() - startTime;
  console.log(
    `[generate-toolbox-registry] tools=${toolSlugs.length} play=${playCount} tiles=${tileEntries.length} → ${OUTPUT_FILE}, ${TOOLS_OUTPUT_FILE}, ${CHEATSHEETS_OUTPUT_FILE}, ${TILE_DEFINITIONS_OUTPUT_FILE} (${elapsed}ms)`,
  );
}

main().catch((err: unknown) => {
  console.error("[generate-toolbox-registry] Fatal error:", err);
  process.exit(1);
});
