    " *",
    " * Generated by: scripts/generate-toolbox-registry.ts",
    " * Regenerate with: npm run generate:toolbox-registry",
    " *",
    " * This file is re-exported by src/cheatsheets/registry.ts.",
    " * Callers should import from src/cheatsheets/registry.ts, not this file.",
    " */",
    "",
    'import type { CheatsheetMeta, CheatsheetDefinition } from "@/cheatsheets/types";',
    "",
    "// --- Cheatsheet imports (auto-discovered from src/cheatsheets/{slug}/meta.ts) ---",
