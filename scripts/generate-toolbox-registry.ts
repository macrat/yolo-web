/**
 * Prebuild script: Generate unified toolbox registry from individual meta.ts files.
 *
 * This script auto-discovers `meta.ts` files under `src/tools/` and `src/cheatsheets/`
 * and generates `src/lib/toolbox/generated/toolbox-registry.ts`.
 *
 * WHY codegen instead of hand-maintained central registry:
 * - Eliminates the class of bug where a developer adds `src/tools/new-tool/meta.ts`
 *   but forgets to register it in `src/lib/toolbox/generated/toolbox-registry.ts`.
 * - At 500+ entries scale, manual registration in a central file is error-prone.
 * - The `meta.ts` file IS the registration — no separate step needed.
 *
 * SCOPE:
 * - tools: `src/tools/{slug}/meta.ts` (named export `meta: ToolMeta`)
 * - cheatsheets: `src/cheatsheets/{slug}/meta.ts` (named export `meta: CheatsheetMeta`)
 * - play: NOT auto-discovered (no per-content meta.ts exists; handled by existing
 *         `src/play/registry.ts` which is imported directly in the generated file)
 *
 * LIMITATION (既知の限界):
 * - この codegen が防ぐのは「toolbox 統合 indexer への未登録」のみ。
 * - 既存の `src/tools/registry.ts` / `src/cheatsheets/registry.ts` は依然として
 *   手書きで管理されており、ツール一覧ページ等への掲載漏れは別問題として残る。
 * - 後続サイクルで既存 registry を段階的に codegen へ統合予定。
 *
 * OUTPUT: `src/lib/toolbox/generated/toolbox-registry.ts`
 * - Committed to git (allows lint/typecheck without running prebuild first)
 * - Regenerated on every prebuild/predev/pretest to stay in sync
 * - If manually edited, run `npm run generate:toolbox-registry` to revert.
 *   The git diff will show the unwanted edits, making them easy to detect and revert.
 */

import fs from "node:fs";
import path from "node:path";
import fg from "fast-glob";
import { allPlayContents } from "../src/play/registry.js";

const ROOT = path.resolve(process.cwd());
const OUTPUT_FILE = path.join(
  ROOT,
  "src/lib/toolbox/generated/toolbox-registry.ts",
);
const OUTPUT_DIR = path.dirname(OUTPUT_FILE);

/** Extract slug from path like "src/tools/{slug}/meta.ts" */
function slugFromPath(filePath: string): string {
  const parts = filePath.split("/");
  // parts = ["src", "tools", "{slug}", "meta.ts"] → index -2 is slug
  return parts[parts.length - 2];
}

/**
 * 件数のサニティチェック。
 * glob の誤設定や予期しないディレクトリ構造変化で件数が激減した場合に早期エラーを出す。
 */
function assertMinCount(kind: string, actual: number, min: number): void {
  if (actual < min) {
    throw new Error(
      `[generate-toolbox-registry] SANITY CHECK FAILED: ` +
        `Expected at least ${min} ${kind} meta.ts files, but found ${actual}. ` +
        `Check src/${kind}/ directory structure.`,
    );
  }
}

/**
 * Generate the TypeScript source content for toolbox-registry.ts.
 *
 * Exported as a pure function so it can be unit-tested in-memory
 * without touching the filesystem (scripts/__tests__/generate-toolbox-registry.test.ts).
 *
 * @param toolSlugs - sorted list of tool slugs discovered from src/tools/{slug}/meta.ts
 * @param cheatsheetSlugs - sorted list of cheatsheet slugs from src/cheatsheets/{slug}/meta.ts
 * @param playCount - count of allPlayContents at generation time (for the comment)
 * @returns TypeScript source string (without trailing newline — caller adds it)
 */
export function buildRegistryContent(
  toolSlugs: string[],
  cheatsheetSlugs: string[],
  playCount: number,
): string {
  const lines: string[] = [
    "/**",
    " * AUTO-GENERATED FILE — DO NOT EDIT MANUALLY.",
    " *",
    " * Generated by: scripts/generate-toolbox-registry.ts",
    " * Regenerate with: npm run generate:toolbox-registry",
    " *",
    " * If you find a manual edit here, run `npm run generate:toolbox-registry`",
    " * to restore. The git diff makes unintended edits easy to detect and revert.",
    " *",
    " * This file merges all tool / play / cheatsheet entries into a unified",
    " * Tileable[] array.  The public API (getAllTileables, getTileableBySlug)",
    " * is exposed via src/lib/toolbox/registry.ts which",
    " * imports from here — callers should never import this file directly.",
    " *",
    " * TOOLS and CHEATSHEETS are auto-discovered from meta.ts files.",
    " * PLAY is imported from the existing src/play/registry.ts because play",
    " * content does not follow the per-slug meta.ts convention.",
    " */",
    "",
    'import type { Tileable } from "../types";',
    'import { toTileable } from "../types";',
    "",
    "// --- Tool imports (auto-discovered from src/tools/{slug}/meta.ts) ---",
  ];

  // Tool imports
  for (const slug of toolSlugs) {
    const importName = `tool_${slug.replace(/-/g, "_")}`;
    lines.push(`import { meta as ${importName} } from "@/tools/${slug}/meta";`);
  }

  lines.push("");
  lines.push(
    "// --- Cheatsheet imports (auto-discovered from src/cheatsheets/{slug}/meta.ts) ---",
  );

  // Cheatsheet imports
  for (const slug of cheatsheetSlugs) {
    const importName = `cheatsheet_${slug.replace(/-/g, "_")}`;
    lines.push(
      `import { meta as ${importName} } from "@/cheatsheets/${slug}/meta";`,
    );
  }

  lines.push("");
  lines.push(
    "// --- Play import (not auto-discovered; uses existing registry) ---",
  );
  lines.push('import { allPlayContents } from "@/play/registry";');
  lines.push("");
  lines.push("// --- Unified Tileable arrays ---");
  lines.push("");
  lines.push(
    "/** All tool Tileables (auto-discovered from src/tools/{slug}/meta.ts). */",
  );
  lines.push("export const toolTileables: Tileable[] = [");
  for (const slug of toolSlugs) {
    const importName = `tool_${slug.replace(/-/g, "_")}`;
    lines.push(`  toTileable(${importName}, "tool"),`);
  }
  lines.push("];");
  lines.push("");
  lines.push(
    "/** All play Tileables (sourced from existing src/play/registry.ts). */",
  );
  lines.push("export const playTileables: Tileable[] = [");
  lines.push('  ...allPlayContents.map((meta) => toTileable(meta, "play")),');
  lines.push("];");
  lines.push("");
  lines.push(
    "/** All cheatsheet Tileables (auto-discovered from src/cheatsheets/{slug}/meta.ts). */",
  );
  lines.push("export const cheatsheetTileables: Tileable[] = [");
  for (const slug of cheatsheetSlugs) {
    const importName = `cheatsheet_${slug.replace(/-/g, "_")}`;
    lines.push(`  toTileable(${importName}, "cheatsheet"),`);
  }
  lines.push("];");
  lines.push("");
  lines.push(
    `// Counts at generation time: tools=${toolSlugs.length}, play=${playCount} (from src/play/registry.ts), cheatsheets=${cheatsheetSlugs.length}`,
  );

  return lines.join("\n");
}

async function main(): Promise<void> {
  const startTime = Date.now();

  // --- Discover meta.ts files ---
  const toolPaths = await fg("src/tools/*/meta.ts", { cwd: ROOT });
  const cheatsheetPaths = await fg("src/cheatsheets/*/meta.ts", {
    cwd: ROOT,
  });

  // Sort for deterministic output (git diff stays clean on re-generation)
  toolPaths.sort();
  cheatsheetPaths.sort();

  const toolSlugs = toolPaths.map(slugFromPath);
  const cheatsheetSlugs = cheatsheetPaths.map(slugFromPath);
  const playCount = allPlayContents.length;

  // --- Sanity checks (early-fail on unexpected structural changes) ---
  assertMinCount("tools", toolSlugs.length, 10);
  assertMinCount("cheatsheets", cheatsheetSlugs.length, 3);

  // --- Generate file content ---
  const content =
    buildRegistryContent(toolSlugs, cheatsheetSlugs, playCount) + "\n";

  // --- Write output ---
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }
  fs.writeFileSync(OUTPUT_FILE, content, "utf-8");

  const elapsed = Date.now() - startTime;
  console.log(
    `[generate-toolbox-registry] tools=${toolSlugs.length} play=${playCount} cheatsheets=${cheatsheetSlugs.length} → ${OUTPUT_FILE} (${elapsed}ms)`,
  );
}

main().catch((err: unknown) => {
  console.error("[generate-toolbox-registry] Fatal error:", err);
  process.exit(1);
});
