/** All valid type IDs for this quiz */
export const CULTURE_TYPE_IDS = [
  "sado",
  "shodo",
  "budo",
  "kado",
  "rakugo",
  "haiku",
  "togei",
] as const;

export type CultureTypeId = (typeof CULTURE_TYPE_IDS)[number];

/**
 * Look up compatibility between two type IDs.
 * Order does not matter: getCompatibility("a","b") === getCompatibility("b","a").
 */
export function getCompatibility(
  typeA: string,
  typeB: string,
): CompatibilityEntry | undefined {
  const key = [typeA, typeB].sort().join("--");
  return compatibilityMatrix[key];
}

/**
 * Check if a given string is a valid culture type ID.
 */
export function isValidCultureTypeId(id: string): id is CultureTypeId {
  return (CULTURE_TYPE_IDS as readonly string[]).includes(id);
}

