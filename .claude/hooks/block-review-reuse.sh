#!/bin/bash

# block-review-reuse.sh (PreToolUse hook / matcher: SendMessage)
# 既存のreviewerエージェントへの「再レビュー」依頼をブロックする。
# 目的: AP-WF20 (レビュアー流用による視野狭窄) / AP-WF10 (SendMessage継続によるコンテキスト劣化) を強制する。
# 修正後の再レビューは、新規のreviewerエージェントをゼロから起動して行うこと。
# (cycle-243で実証: 流用レビューが見逃した事実誤りを、白紙の新規レビューが検出した)

INPUT=$(cat)
TEXT=$(echo "$INPUT" | jq -r '.tool_input | tostring' 2>/dev/null)
[ -z "$TEXT" ] && exit 0

# 宛先が不透明なID (reviewer の語を含まない) の場合も検出できるように、宛先や
# エージェント種別には依存せず「再レビュー依頼の表現そのもの」でブロックする。
# ただし「再確認」「もう一度確認」のようなレビュー以外でも日常的に使う表現は
# 誤検出が多いため含めず、「レビュー」を明示する表現に限定する。
REREVIEW_PATTERN='再レビュー|再度(の)?レビュー|もう一度レビュー|レビューをやり直|レビューし直|レビューを再依頼|改めてレビュー'

if echo "$TEXT" | grep -qE "$REREVIEW_PATTERN"; then
  cat >&2 << 'EOF'
[BLOCKED] 既存エージェントへの再レビュー依頼を検出しました (AP-WF20 / AP-WF10)。

修正後の再レビューは、既存レビュアーへのSendMessage継続ではなく、
新規のreviewerエージェントをゼロから起動して行ってください。
このとき、対象ファイルとレビュー観点のみを渡し、前回の指摘・経緯は渡さないでください
(流用レビューは「前回指摘が直ったか」に視野が狭まり、全体の白紙見直しができません)。

SendMessageは、同一タスクに対する軽微な修正依頼・追加質問のみに使用してください。
再レビュー以外の正当な内容がブロックされた場合は、表現を言い換えて送信してください。
EOF
  exit 2
fi

exit 0
